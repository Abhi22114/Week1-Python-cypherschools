  
#escape sequence in python and meaning
# (  \' single quote )
#(  \"  double quote )
#(  \\ backslash )
#(  \n  new line )
#(  \t  tab  )
#(  \b  backspace  )


#single and double quote backslash 
print("hello \" vedant\"heloo")
print('hello\'abhijeet\'heloo')
print('I\'m abhijeet')
print("vedant\"s father")

# new line 
print("line A")
print("line B")
print("Abhijeet\n Raja\n Abhinav")  # \n for new line
#\t tab
print("My name is\tAbhijeet")
 
#doble backslash (\\)
print("My name is Abhijeet\\")
print("My name is Abhijeet\\\\")

# backspace  (  \b  )
print("abhi\bjeet")
print('ab\bhijeet')